// We can assign value to val and var both like given below.


fun main() {
    var name:String="Sachin"
    println("Hello $name")

    var age:Int=42
    println(age)

    var isMarried:Boolean=true
    println(isMarried)

    var pi:Double=3.14
    println(pi)

    var float:Float=6.5F
    println(float)
}