// Var is an abbreviation of 'variable'. You use this modifier to specify
// a variable that can be used in the program. So there value can change
// when the code runs



// var can only be reassigned its previous type only
// You cannot assign int to string type or boolean to string type var.

fun main() {
    var name= "John"
    name = "Doe"
    // name=123
    // name=true    ERROR!
    
    println(name)
    
}
